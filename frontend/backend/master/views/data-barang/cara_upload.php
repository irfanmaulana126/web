    Download file template
    File ini memiliki kolom heading sesuai dengan yang Jurnal perlukan untuk mengimpor data Anda dengan benar.

    Copy data lama anda
    Gunakan Microsoft Excel atau aplikasi Spreadsheet lainnya untuk men-copy dan paste data penjualan lama Anda dari file yang Anda ekspor sebelumnya ke dalam Template Jurnal. Pastikan bahwa data penjualan yang Anda input sesuai dengan heading kolom yang disediakan di dalam template.
    Lihat screenshot
    Klik disini untuk melihat tutorial lengkap petunjuk cara import
    PENTING!
    - Format Tanggal : dd/mm/yyyy (English - Canada)
    - Maksimum baris transaksi adalah 1000 entry (optimal)
    - TIDAK perlu pemisah untuk nominal ribuan seperti koma (,) atau titik (.)
    - Gunakan titik (.) sebagai pemisah desimal
    - TIDAK perlu memasukkan simbol mata uang (Rp, $, dll)
    - JANGAN ubah format kolom yang disediakan
    Tips:
    Jika menggunakan Microsoft Excel, gunakan tanda (') didepan pada setiap pengisian data yang menggunakan angka. Contoh : `6-6003, `11-02-2016

    Upload file template
    Setelah selesai mengubah, silahkan upload file. File yang akan Anda upload harus dalam format CSV (Comma Separated Values). File anda harus diakhiri dengan ekstensi .csv, .xlsx atau .xls
